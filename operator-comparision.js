const a = 100;
const b = 100;

// equal to (==) [ equal or not but not datatype]
console.log(a == b);
// equal to with data type [ === ]
console.log(a === b); // important

const c = 100;
// greater than and greater than equal too
console.log(c > 100); // false (100 > 100)
console.log(c >= 100); // true (100 >= 100)

// Less than and Less than equal too
console.log(c < 100); // false (100 < 100)
console.log(c <= 100); // true (100 <= 100)

// Not equals without dataType ( != )
const i = 100;
console.log(i != "100"); // false
// Not equals with dataType ( !== )
console.log(i !== "100"); // (100 !== "100") // important
