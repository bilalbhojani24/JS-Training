let x = 10;
let y = 20;

// assingment operator
z = y;
console.log(z);

// add
x += 50; // as same x = x + 50
console.log(x); // 60

// substract
x -= 50; // as same as x = x - 50
console.log(x); // 10

// multiplication
x *= 50; // as same as x = x * 50
console.log(x); // 500

// Division
x /= 50; // as same as x = x / 50
console.log(x); // 10

// Mod
x %= 50; // as same as x = x % 50
console.log(x);
