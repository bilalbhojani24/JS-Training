const num = 2.914;
console.log(num);

console.log(Math.round(num)); // JS will take decision (universal)
console.log(Math.floor(num)); // It will round to lowest integer
console.log(Math.ceil(num)); // It will round to highest integer
console.log(Math.pow(3, 3)); // 8
console.log(Math.sqrt(2)); // 4
console.log(Math.min(2, 1, -0.9, 89, -0.009));
console.log(Math.max(2, 1, -0.9, 89, -0.009));
console.log(Math.random()); // create num between 0 - 1

